export const applyLife = (board, lifetype) => {
  const life = lifeforms[lifetype] || lifeforms.glider
  if (board.length < life.length || board[0].length < life[0].length) {
    return board
  }
  return board.map((row, i) => row.map((cell, j) => cell || (life[i] && life[i][j])))
}

export const applyRandomLifeform = (board) => {
  const options = []
  if (board.length >= 5 && board[0].length >= 5) options.push('blinker')
  if (board.length >= 6 && board[0].length >= 6) options.push('toad', 'beacon')
  if (board.length >= 7 && board[0].length >= 7) options.push('glider', 'lwss')
  if (board.length >= 14 && board[0].length >= 14) options.push('pulsarOscilator')
  if (board.length >= 5 && board[0].length >= 18) options.push('pentadecathlon')
  return applyLife(board, sample(options))
}
